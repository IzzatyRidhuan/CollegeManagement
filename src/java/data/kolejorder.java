public class kolejorder {
    private String studentID, status, kolejname, block;
    private int orderID, kolejID;

    /**
     * @return the studentID
     */
    public String getStudentID() {
        return studentID;
    }

    /**
     * @param studentID the studentID to set
     */
    public void setStudentID(String studentID) {
        this.studentID = studentID;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the kolejname
     */
    public String getKolejname() {
        return kolejname;
    }

    /**
     * @param kolejname the kolejname to set
     */
    public void setKolejname(String kolejname) {
        this.kolejname = kolejname;
    }

    /**
     * @return the block
     */
    public String getBlock() {
        return block;
    }

    /**
     * @param block the block to set
     */
    public void setBlock(String block) {
        this.block = block;
    }

    /**
     * @return the orderID
     */
    public int getOrderID() {
        return orderID;
    }

    /**
     * @param orderID the orderID to set
     */
    public void setOrderID(int orderID) {
        this.orderID = orderID;
    }

    /**
     * @return the kolejID
     */
    public int getKolejID() {
        return kolejID;
    }

    /**
     * @param kolejID the kolejID to set
     */
    public void setKolejID(int kolejID) {
        this.kolejID = kolejID;
    }
    
    
}
